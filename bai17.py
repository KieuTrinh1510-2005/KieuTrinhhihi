a=int(input("Nhập số thứ nhất :"))
b=int(input("Nhập só thứ hai :"))
c=int(input("Nhập số thứ ba :"))
solonnhat=max(a,b,c)
sonhonhat=min(a,b,c)
print("Số lớn nhất trong 3 số:",solonnhat)
print("Số bé nhất trong 3 số:",sonhonhat)

